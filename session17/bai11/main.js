let number1 = Math.abs(-5.5);  
console.log(number1);
// làm cho giá trị âm được chuyển đổi sang giá trị dương

let number2 = Math.abs(10);
console.log(number2);
// khi giá trị là một số dương nó sẽ được giữ nguyên nếu là số âm sẽ chuyển thành số nguyên 

